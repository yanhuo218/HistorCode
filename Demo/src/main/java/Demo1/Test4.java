    public static  char[][] rotateTheBox(char[][] box) {
        int line = box.length; //行
        int column = box[0].length; //列
        char[][] chars = new char[column][line];
        for (int i = 0; i < column; i++) {
            for (int j = 0; j < line; j++) {
                chars[i][j] = box[line-1-j][i];
            }
        }


        for (int j = 0; j < line; j++) {
            for (int i = column-1; i >= 0; --i) {
                if (chars[i][j] == '.'){
                    if (i==0){
                        break;
                    }
                    if (chars[i-1][j] == '#') {
                        char x = chars[i][j];
                        chars[i][j] = chars[i - 1][j];
                        chars[i - 1][j] = x;
                        i = column-1;
                    }
                }
            }
        }
        return chars;
    }
